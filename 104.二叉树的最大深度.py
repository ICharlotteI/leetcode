# [104] 二叉树的最大深度
#

# @lc code=start
# Definition for a binary tree node.
from typing import Optional


class TreeNode:
    def __init__(self, val=0, left=None, right=None):
        self.val = val
        self.left = left
        self.right = right


class Solution:
    def maxDepth(self, root: Optional[TreeNode]) -> int:
        if not root:
            return 0

        left_length = self.maxDepth(root.left)
        right_lenght = self.maxDepth(root.right)
        return max(left_length, right_lenght) + 1


